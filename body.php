                    <h2>Your resume should be</h2>
                    <ul>
                        <li>Clear and concise</li>
                        <li>Focused</li>
                        <li>Written with action verbs</li>
                        <li>Accomplishment oriented</li>
                        <li>Honest</li>
                        <li>Prepared by YOU</li>
                        <li>No more than two (in rare cases, three) pages</li>
                        <li>Printed on quality paper</li>
                        <li>Proofed - and proofed again!</li>
                    </ul>
                </div>
            </div>
            <div class="col2 last" style="border-bottom: 0 none;">
