                <div class="the-text resume">
                    <h2>It should not contain</h2>
                    <ul>
                        <li>Your photo</li>
                        <li>Birth date</li>
                        <li>Marital status</li>
                        <li>Social Insurance Number (SIN)</li>
                        <li>Salary expectations</li>
                        <li>Reason for leaving</li>
                        <li>Reference list</li>
                        <li>Any reference to political or religious beliefs</li>
                        <li>Personal hobbies and interests, <br />
                        <small>unless they highlight skills transferable<br /> 
                        to the workplace</small></li>
                        <li>Technical jargon or abbreviations</li>
